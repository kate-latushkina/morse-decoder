const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str = '';
    let newStr = '';
    for (let i = 0; i < expr.length; i++) {
        str += expr[i];
        if (str.length == 10) {
            if (str == '**********') {
                newStr += ' ';
                str = '';
            }
            else {
                str = str.replace(/10/g, '.').replace(/11/g, '-').replace(/0/g, '');
                newStr += MORSE_TABLE[str];
                str = '';
            }
        }
    }
    return newStr;
}

module.exports = {
    decode
}
